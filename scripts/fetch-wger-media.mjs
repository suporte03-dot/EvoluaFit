const delay = (ms) => new Promise((r) => setTimeout(r, ms))

async function searchWger(term) {
  const url = `https://wger.de/api/v2/exerciseinfo/?language=2&search=${encodeURIComponent(term)}&limit=5`
  const res = await fetch(url)
  if (!res.ok) throw new Error(`wger ${res.status} for ${term}`)
  const data = await res.json()
  return data.results || []
}

function pickBestImage(results, searchTerm) {
  const terms = searchTerm.toLowerCase().split(/\s+/)
  let best = null
  let bestScore = -1

  for (const ex of results) {
    const images = ex.images?.filter((img) => img.image) || []
    if (!images.length) continue

    const name = (ex.translations?.find((t) => t.language === 2)?.name || ex.name || '').toLowerCase()
    let score = 0
    for (const t of terms) {
      if (name.includes(t)) score += 2
    }
    if (images.length) score += 1

    if (score > bestScore) {
      bestScore = score
      best = {
        image: images[0].image.startsWith('http') ? images[0].image : `https://wger.de${images[0].image}`,
        wgerName: name,
        wgerId: ex.id,
      }
    }
  }

  if (!best && results[0]?.images?.[0]?.image) {
    const img = results[0].images[0].image
    best = {
      image: img.startsWith('http') ? img : `https://wger.de${img}`,
      wgerName: results[0].translations?.[0]?.name || '',
      wgerId: results[0].id,
    }
  }

  return best
}

async function main() {
  const map = {}
  const meta = {}
  const ids = Object.keys(EXERCISE_SEARCH_TERMS)
  let found = 0

  for (const id of ids) {
    const term = EXERCISE_SEARCH_TERMS[id]
    try {
      const results = await searchWger(term)
      const picked = pickBestImage(results, term)
      if (picked?.image) {
        map[id] = picked.image
        meta[id] = { search: term, wgerName: picked.wgerName, wgerId: picked.wgerId }
        found++
        console.log(`✓ ${id}: ${picked.image}`)
      } else {
        console.warn(`✗ ${id}: no image for "${term}"`)
      }
    } catch (err) {
      console.warn(`✗ ${id}: ${err.message}`)
    }
    await delay(400)
  }

  const outPath = join(__dirname, '../src/data/exerciseGifMap.js')
  const content = `/**
 * Real demonstrative media URLs (wger.de CC-BY-SA).
 * Generated by scripts/fetch-wger-media.mjs — re-run to refresh.
 * @see https://wger.de
 */

/** @type {Record<string, string>} id → image URL */
export const EXERCISE_GIF_MAP = ${JSON.stringify(map, null, 2)}

/** @type {Record<string, { search: string, wgerName?: string, wgerId?: number }>} */
export const EXERCISE_MEDIA_META = ${JSON.stringify(meta, null, 2)}

export function getExerciseGifUrl(id) {
  return EXERCISE_GIF_MAP[id] || null
}

export function hasRealMedia(id) {
  return Boolean(EXERCISE_GIF_MAP[id])
}

export const REAL_MEDIA_COUNT = ${found}
export const TOTAL_MAPPED = ${ids.length}
`

  writeFileSync(outPath, content, 'utf8')
  console.log(`\nDone: ${found}/${ids.length} exercises with real media → ${outPath}`)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
